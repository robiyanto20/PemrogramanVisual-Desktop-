    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabel_prodi.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            String idProdi = tabel_prodi.getValueAt(selectedRow, 1).toString();

            try {
                deleteDataProdi(idProdi);
                tampilData(); // Refresh tampilan tabel setelah dihapus
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data Prodi. \n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        try {
            String idJurusan = TFidjurusan.getText();
            String namaJurusan = TFnamajurusan.getText();

            // Validasi input
            if (idJurusan.isEmpty() || namaJurusan.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Isi semua field!");
                return;
            }

            // Buat objek statement untuk mengeksekusi query SQL
            st = conn.createStatement();

            // Buat query untuk menambahkan data ke tabel
            String sql = "INSERT INTO tbl_prodi (id_prodi, prodi) VALUES ('" + idJurusan + "', '" + namaJurusan + "')";

            // Eksekusi query
            st.executeUpdate(sql);

            // Tampilkan pesan berhasil
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");

            // Refresh tampilan tabel
            tampilData();

            // Kosongkan field setelah tambah data
            TFidjurusan.setText("");
            TFnamajurusan.setText("");

        } catch (SQLException e) {
            // Tangani kesalahan SQL
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data. \n" + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        if (idProdiToUpdate == null || idProdiToUpdate.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah!");
            return;
        }

        try {
            // Buat query untuk update data
            String sql = "UPDATE tbl_prodi SET prodi=? WHERE id_prodi=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, TFnamajurusan.getText());
            ps.setString(2, idProdiToUpdate);

            // Eksekusi query update
            int hasil = ps.executeUpdate();

            // Tampilkan pesan sukses
            if (hasil > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                tampilData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

