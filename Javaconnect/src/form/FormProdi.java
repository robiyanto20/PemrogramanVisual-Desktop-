    private void deleteDataProdi(String idProdi) throws SQLException {
        String queryDelete = "DELETE FROM tbl_prodi WHERE id_prodi = ?";

        try (PreparedStatement preparedStatement = conn.prepareStatement(queryDelete)) {
            preparedStatement.setString(1, idProdi);

            preparedStatement.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Prodi berhasil dihapus.");
        }
    }
    
    private void tampilData() {
    DefaultTableModel kolomtabel = new DefaultTableModel();
    kolomtabel.addColumn("No.");
    kolomtabel.addColumn("ID Prodi");
    kolomtabel.addColumn("Prodi");
    try {
        int nomor = 1;
        // buat objek statement untuk mengeksekusi query mysql.
        st = conn.createStatement();
        // buat query ke database
        String sql = "SELECT * FROM tbl_prodi";
        // eksekusi query dan simpan hasilnya di objek ResultSet
        rs = st.executeQuery(sql);
        // tampilkan hasil query menggunakan perulangan while
        while (rs.next()) {
            kolomtabel.addRow(new Object[]{
                ("" + nomor++),
                rs.getString("id_prodi"),
                rs.getString("prodi")
            });
            tabel_prodi.setModel(kolomtabel);
            tabel_prodi.enable(true);
            TFnamajurusan.requestFocus();
        }
            tabel_prodi.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = tabel_prodi.rowAtPoint(evt.getPoint());
                idProdiToUpdate = tabel_prodi.getValueAt(row, 1).toString();
                TFidjurusan.setText(idProdiToUpdate);
                TFnamajurusan.setText(tabel_prodi.getValueAt(row, 2).toString());
            }
        });
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data. \n" + e.getMessage());
    }
}

    
